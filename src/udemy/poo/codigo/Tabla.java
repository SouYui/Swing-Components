    private void guardarButtonActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_guardarButtonActionPerformed
        // TODO add your handling code here:
        Archivo objetoArchivo;

        JFileChooser directorio = new JFileChooser();
        directorio.setFileSelectionMode(JFileChooser.FILES_AND_DIRECTORIES);

        int selection = directorio.showSaveDialog(jPanel1);

        if (selection == JFileChooser.APPROVE_OPTION) {
            try {
                File archivo = directorio.getSelectedFile();
                objetoArchivo = new Archivo(archivo, this.tabla.getDatos());
                objetoArchivo.almacenar();
            } catch (IOException ex) {
                JOptionPane.showMessageDialog(rootPane, "Error al guardar el archivo",
                        "Status de archivo errÃ³neo", JOptionPane.ERROR_MESSAGE);
            }
        }
    }//GEN-LAST:event_guardarButtonActionPerformed

    private void cargarButtonActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_cargarButtonActionPerformed
        // TODO add your handling code here:
        Archivo objetoArchivo;

        JFileChooser directorio = new JFileChooser();
        directorio.setFileSelectionMode(JFileChooser.FILES_AND_DIRECTORIES);

        int selection = directorio.showSaveDialog(jPanel1);

        if (selection == JFileChooser.APPROVE_OPTION) {
            try {
                File archivo = directorio.getSelectedFile();
                objetoArchivo = new Archivo(archivo, this.tabla.getDatos());
                this.tabla.setDatos(objetoArchivo.cargar());
                this.tabla.fireTableDataChanged();
            } catch (IOException ex) {
                JOptionPane.showMessageDialog(rootPane, "Error al abrir los datos",
                        "Status de archivo errÃ³neo (No object)", JOptionPane.ERROR_MESSAGE);
            } catch (ClassNotFoundException ex) {
                JOptionPane.showMessageDialog(rootPane, "No se encontrÃ³ el archivo",
                        "Status de archivo errÃ³neo", JOptionPane.ERROR_MESSAGE);
            }
        }
    }//GEN-LAST:event_cargarButtonActionPerformed

