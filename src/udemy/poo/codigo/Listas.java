    private void intervaloButtonActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_intervaloButtonActionPerformed
        // Seleccionar los elementos con la tecla Shitf
        
        List<String> elementos = listaIntervalo.getSelectedValuesList();
        String datos = "";
        
        for (Object elemento : elementos) {
            datos = datos + elemento + "\n";
        }
        
        JOptionPane.showMessageDialog(rootPane, datos);
    }//GEN-LAST:event_intervaloButtonActionPerformed

