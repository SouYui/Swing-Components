    private void convertir() {
        String texto = verifiacionField.getText();
        
        if (mayusculasBox.isSelected()) {
            verifiacionField.setText(texto.toUpperCase());
        } else {
            verifiacionField.setText(texto.toLowerCase());
        }
        
        verifiacionField.requestFocusInWindow();
    }
    
    private void cambiarA() {
        String texto = verifiacionField.getText();
        
        if (cambioBox.isSelected()) {
            this.temporal = verifiacionField.getText();
            verifiacionField.setText(texto.replace('a', 'A'));
        } else {
            verifiacionField.setText(temporal);
        }
        verifiacionField.requestFocusInWindow();
    }
    
